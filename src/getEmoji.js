module.exports = function (name) {
  switch (name) {
    case 'Hoshimachi Suisei':
      return '☄️'
    case 'Minato Aqua':
      return '⚓️'
    case 'Takanashi Kiara':
      return '🐔'
    case 'Shirakami Fubuki':
      return '🌽'
    case 'Nakiri Ayame':
      return '👿'
    case 'Nekomata Okayu':
      return '🍙'
  }
}